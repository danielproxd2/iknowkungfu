import { HARNESS_VERSION, blockHash, type DocContext, type GeneratedArtifact } from "@iknowkungfu/core";
import type { Client } from "@iknowkungfu/schemas";

export const MCP_JSON_SNIPPET = `{"mcpServers":{"iknowkungfu":{"command":"iknowkungfu","args":["mcp"]}}}`;

const SHIM_BUDGET = 30;

function shim(
  id: string,
  path: string,
  ownership: GeneratedArtifact["ownership"],
  content: string,
): GeneratedArtifact {
  const warnings: string[] = [];
  if (content.split("\n").length > SHIM_BUDGET) warnings.push(`${path} over shim budget (${SHIM_BUDGET} lines)`);
  if (ownership === "owned-verbatim") {
    return { id, path, blocks: [], ownership, verbatim: content, lineBudget: SHIM_BUDGET, warnings };
  }
  return {
    id,
    path,
    blocks: [{ id, inputs: blockHash(content), content }],
    ownership,
    lineBudget: SHIM_BUDGET,
    warnings,
  };
}

function agentsMd(): GeneratedArtifact {
  return shim(
    "agents-md",
    "AGENTS.md",
    "managed-blocks",
    [
      "# Agent instructions (generated by iknowkungfu — run `iknowkungfu refresh` if stale)",
      "1. Read `.iknowkungfu/docs/PROJECT_CONTEXT.md` for structure and EXACT commands.",
      "2. Follow the loop in `.iknowkungfu/docs/AGENT_RUNBOOK.md` — verify before and after edits.",
      "3. Check `.iknowkungfu/docs/REFACTOR_GUARDRAILS.md` before touching listed risk areas.",
      "4. Validate with `iknowkungfu verify --changed`; gate commits with `iknowkungfu risk --staged`.",
      "5. If the `iknowkungfu` MCP server is available, prefer its tools over manual exploration.",
      "",
      "Facts tagged `⚠ inferred` or `🤖 llm` need verification; untagged facts are parsed from config — trust them.",
    ].join("\n"),
  );
}


function copilotInstructions(): GeneratedArtifact {
  return shim(
    "copilot",
    ".github/copilot-instructions.md",
    "managed-blocks",
    [
      "This repo uses iknowkungfu. Project facts and exact build/test/lint commands:",
      "`.iknowkungfu/docs/PROJECT_CONTEXT.md`. Workflow: `.iknowkungfu/docs/AGENT_RUNBOOK.md`.",
      "Validate changes with `iknowkungfu verify --changed`. Do not invent commands.",
    ].join("\n"),
  );
}

const REGISTRY: Record<Client, (ctx: DocContext) => GeneratedArtifact> = {
  "agents-md": agentsMd,
  claude: claudeMd,
  codex: codexSkill,
  cursor: cursorRule,
  copilot: copilotInstructions,
};

export function adapterArtifact(client: Client, ctx: DocContext): GeneratedArtifact {
  return REGISTRY[client](ctx);
}

export function adapterPath(client: Client): string {
  // Paths are static per client; build a throwaway artifact to read them.
  return REGISTRY[client]({} as DocContext).path;
}
